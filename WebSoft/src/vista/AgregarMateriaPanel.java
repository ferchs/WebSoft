    private boolean validarCamposPanel( ){
        boolean datosCompletos=true;
      if(codigo.getText().isEmpty()){
          try{
          codigoT.setForeground(Color.red);
          Integer.parseInt(nombre.getText().trim());
          }
          catch(Exception e){
            codigo.setText("Debe ser un numero");
            datosCompletos=false;
          }
      }
      if(nombre.getText().isEmpty()){
          nombreT.setForeground(Color.red);
          datosCompletos=false;
      }
      
      return datosCompletos;
    }
    
    private void limpiarCampos(){
        codigo.setText("");
        codigoT.setForeground(Color.WHITE);
        nombre.setText("");
        nombreT.setForeground(Color.WHITE);
    }
    
