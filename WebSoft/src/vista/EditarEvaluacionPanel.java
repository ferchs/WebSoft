    private boolean validarCamposPanel( ){
        boolean datosCompletos=true;
      if(grado.getSelectedIndex()==0){
          gradoT.setForeground(Color.red);
          datosCompletos=false;
      }
      else{
          gradoT.setForeground(Color.WHITE);
      }
      if(consecutivo.getSelectedIndex()==0){
          consecutivoT.setForeground(Color.red);
          datosCompletos=false;
      }
      else{
        consecutivoT.setForeground(Color.WHITE);
      }
      if(tema.getText().isEmpty()){
          temaT.setForeground(Color.red);
          datosCompletos=false;
      }
      else{
          temaT.setForeground(Color.WHITE);
      }
      if(porcentaje.getText().isEmpty()){
          porcentajeT.setForeground(Color.red);
          datosCompletos=false;
      }
      else{
          porcentajeT.setForeground(Color.WHITE);
      }
      return datosCompletos;
    }
    
    private void limpiarCampos(){
        grado.setSelectedIndex(0);
        gradoT.setForeground(Color.WHITE);
        consecutivo.setSelectedIndex(0);
        consecutivoT.setForeground(Color.WHITE);
        materia.setSelectedIndex(0);
        materiaT.setForeground(Color.WHITE);
        tema.setText("");
        temaT.setForeground(Color.WHITE);
        porcentaje.setText("");
        porcentajeT.setForeground(Color.WHITE);
    }
    
    
    private final void cargarGrados(){
        String [] listadoGrados=controlGestionarEvaluaciones.obtenerGradosImpartidos();
        if(listadoGrados!=null){
        listadoGrados[0]="Seleccione...";
        DefaultComboBoxModel grados= new DefaultComboBoxModel(listadoGrados);
        grado.setModel(grados);
        }
        else{
            listadoGrados=new String[1];
            listadoGrados[0]="Seleccione...";
            DefaultComboBoxModel grados= new DefaultComboBoxModel(listadoGrados);
            grado.setModel(grados);
        }
    }
    
    public void llenarCampos(Evaluaciones evaluacion){
        MostrarItem(grado,evaluacion.getCursosImpartidos().getCursos().getGrados().getNombre());
        MostrarItem(consecutivo,Integer.toString(evaluacion.getCursosImpartidos().getCursos().getCursosPK().getConsecutivo()));
        String nombreMateria=evaluacion.getCursosImpartidos().getMaterias().getNombre()+" - "
                +evaluacion.getCursosImpartidos().getMaterias().getCodigoMateria();
        String[] materias=new String[2];
        materias[0]="Seleccione...";
        materias[1]=nombreMateria;
        DefaultComboBoxModel grados= new DefaultComboBoxModel(materias);
        materia.setModel(grados);       
        tema.setText(evaluacion.getEvaluacionesPK().getTema());
        MostrarItem(materia,nombreMateria);
        porcentaje.setText(convertirDoubleInt(evaluacion.getPorcentaje()));
    }
        
     private void MostrarItem(JComboBox comboBox, String itemSeleccionado){
         int num = comboBox.getItemCount();
         for (int i = 0; i < num; i++) {
            String item = (String) comboBox.getItemAt(i);
            if(itemSeleccionado.equals(item)){
                comboBox.setSelectedIndex(i);
            }
         }
     }
     
     private String convertirDoubleInt(double porcentaje){
         int resultado=(int) (porcentaje*100);
         return Integer.toString(resultado);
     }

