    private void guardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_guardarActionPerformed
        // TODO add your handling code here:
        boolean validos=validarCamposPanel();
        if(validos){
            String gradoStr=(String) grado.getSelectedItem();
            String consecutivoStr=(String) consecutivo.getSelectedItem();
            int consecutivoTmp=Integer.parseInt(consecutivoStr);
            HashMap<String,String> datosCurso=controlAdministrarCursos.obtenerDatosCurso(gradoStr, consecutivoTmp);
            int idGrado=Integer.parseInt(datosCurso.get("idGrado"));
            String item=(String)materia.getSelectedItem();
            int index=item.indexOf("-");
            String tmp=item.substring(index+1,item.length()).trim();
            int idMateria=Integer.parseInt(tmp);
            String temaTmp= tema.getText().trim();
            double porcentajeTmp=obternerPorcentaje(porcentaje.getText());
            controlGestionarEvaluaciones.editarEvaluacion(idGrado, consecutivoTmp, idMateria, temaTmp, porcentajeTmp);
            JOptionPane mensaje= new JOptionPane();
            mensaje.setBackground(new java.awt.Color(102, 102, 102));
            mensaje.setForeground(Color.white);
            mensaje.showMessageDialog(null,"Se ha modificado el porcentaje de la evaluacion");
            limpiarCampos();
        }
        else{
            JOptionPane mensaje= new JOptionPane();
            mensaje.setBackground(new java.awt.Color(102, 102, 102));
            mensaje.setForeground(Color.white);
            mensaje.showMessageDialog(null,"Debes completar los campos indicados");
        }
    }//GEN-LAST:event_guardarActionPerformed

    private double obternerPorcentaje(String porcentaje){
        float porcent=Float.parseFloat(porcentaje);
        double resultado=porcent/100;
        return resultado;
    }
    
