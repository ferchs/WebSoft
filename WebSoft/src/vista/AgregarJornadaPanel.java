    private void guardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_guardarActionPerformed
        // TODO add your handling code here:
        boolean validos=validarCamposPanel();
        if(validos){
            int idJornadaTmp=Integer.parseInt(idJornada.getText().trim());
            String nombreJornadaTmp=nombreJornada.getText().trim();
            String horaInicioTmp=(String)horaInicio.getSelectedItem();
            String horaFinTmp=(String)horaFin.getSelectedItem();
            controlAdministrarSalones.agregarJornada(idJornadaTmp, nombreJornadaTmp, horaInicioTmp, horaFinTmp);
            JOptionPane mensaje= new JOptionPane();
            mensaje.setBackground(new java.awt.Color(102, 102, 102));
            mensaje.setForeground(Color.white);
            mensaje.showMessageDialog(null,"Se ha registrado una nueva jornada");
            limpiarCampos();
        }
        else{
            JOptionPane mensaje= new JOptionPane();
            mensaje.setBackground(new java.awt.Color(102, 102, 102));
            mensaje.setForeground(Color.white);
            mensaje.showMessageDialog(null,"Debes completar los campos indicados");
        }
    }//GEN-LAST:event_guardarActionPerformed

