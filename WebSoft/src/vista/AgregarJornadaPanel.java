    private boolean validarCamposPanel( ){
        boolean datosCompletos=true;
      if(idJornada.getText().isEmpty()){
          try{
          idJornadaT.setForeground(Color.red);
          int idSalonTmp=Integer.parseInt(idJornada.getText().trim());
          }
          catch(Exception e){
            idJornada.setText("El Id debe ser un numero");
            datosCompletos=false;
          }
      }
      if(nombreJornada.getText().isEmpty()){
          nombreJornadaT.setForeground(Color.red);
          datosCompletos=false;
      }
      if(horaInicio.getSelectedIndex()==0){
          horaInicioT.setForeground(Color.red);
          datosCompletos=false;
      }
      if(horaFin.getSelectedIndex()==0){
          horaFinT.setForeground(Color.red);
          datosCompletos=false;
      }
      return datosCompletos;
    }
    
    private void limpiarCampos(){
        idJornada.setText("");
        idJornadaT.setForeground(Color.WHITE);
        nombreJornada.setText("");
        nombreJornadaT.setForeground(Color.WHITE);
        horaInicio.setSelectedIndex(0);
        horaInicioT.setForeground(Color.WHITE);
        horaFin.setSelectedIndex(0);
        horaFinT.setForeground(Color.WHITE);
    }
    
